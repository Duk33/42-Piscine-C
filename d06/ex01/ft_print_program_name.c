void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
	return ;
}

int		main(int argc, char **argv)
{
	ft_putstr(argv[0]);
	return (0);
}
