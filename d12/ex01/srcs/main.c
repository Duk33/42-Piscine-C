#include "lib_ft.h"

int		main(int ac, char **av)
{
	int i;

	i = 1;
	if (ac >= 2)
	{
		while (i < ac)
		{
			ft_puterror(av[i], ft_cat(av[i]));
			i++;
		}
		return (0);
	}
	else
		return (1);
}
